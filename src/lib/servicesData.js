export function getServiceBySlug(slug) {
  return servicesData.find((s) => s.slug === slug) || null;
}

export function getServicesForList() {
  return servicesData.map(({ slug, title, subTitle, iconUrl }) => ({
    title,
    subTitle,
    iconUrl,
    href: `/services/${slug}`,
  }));
}

export function getTopServices(limit = 5) {
  const topSlugs = [
    "gynecologie-obstetrique",
    "maternite",
    "soins-infertilite",
    "consultation-gynecologique",
    "chirurgie",
  ];
  return topSlugs
    .slice(0, limit)
    .map((slug) => {
      const service = getServiceBySlug(slug);
      return service
        ? {
            title: service.title,
            subTitle: service.subTitle,
            iconUrl: service.iconUrl,
            href: `/services/${slug}`,
          }
        : null;
    })
    .filter(Boolean);
}
